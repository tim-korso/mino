# ============================================================
#  workplace.ps1 - Workplace automation module
#  Mirror of macOS workplace automation pipelines
#
#  Commands: brief | email | weekly | organize | push | research
# ============================================================

function Invoke-WorkplaceCommand {
    param(
        [Parameter(Mandatory)]
        [ValidateSet('brief','email','weekly','organize','push','research')]
        [string]$Command,
        [string]$Extra
    )
    switch ($Command) {
        'brief'    { Invoke-WorkplaceBrief @(if ($Extra) { $Extra -split '\s+' } else { @() }) }
        'email'    { Invoke-WorkplaceEmail }
        'weekly'   { Invoke-WorkplaceWeekly }
        'organize' { Invoke-WorkplaceOrganize }
        'push'     { Invoke-WorkplacePush -Target $Extra }
        'research' { Invoke-WorkplaceResearch -Query $Extra }
    }
}

# --- brief: Morning financial briefing ---
function Invoke-WorkplaceBrief {
    param([string[]]$Args)
    Write-Banner 'Morning Brief Generator'

    $date = Get-Date -Format 'yyyy-MM-dd'
    $outputDir = Join-Path $script:HubRoot '..\briefs'
    if (-not (Test-Path $outputDir)) { New-Item -ItemType Directory -Path $outputDir -Force | Out-Null }

    $outputFile = Join-Path $outputDir "brief-$date.md"

    Write-Host '  Morning Brief Template' -ForegroundColor Yellow
    Write-Host '  ========================' -ForegroundColor Yellow
    Write-Host ''
    Write-Host "  Date: $date" -ForegroundColor White
    Write-Host ''

    $sections = @(
        @{Title='1. Major Financial Cases / Risk Events'; Icon='[!!]'},
        @{Title='2. Interest Rate Changes (PBOC, LPR)'; Icon='[$$]'},
        @{Title='3. Financial Innovation (Tech/Green/Inclusive)'; Icon='[++]'},
        @{Title='4. New Policies / Regulations'; Icon='[..]'}
    )

    foreach ($s in $sections) {
        Write-Host "  $($s.Icon) $($s.Title)" -ForegroundColor Cyan
        Write-Host ('  ' + '-' * 40) -ForegroundColor Gray
        Write-Host ''
    }

    Write-Host "  ---" -ForegroundColor Gray
    Write-Host "  This module generates the structure. AI fills the content." -ForegroundColor Gray
    Write-Host "  For auto-generation, set up a myagents cron task:" -ForegroundColor Gray
    Write-Host "    myagents cron add --name Mino-Morning-Brief --prompt-file brief-prompt.txt --schedule '30 6 * * 1-5'" -ForegroundColor DarkGray
    Write-Host ''

    # Write template to file
    $template = @"
# Morning Financial Brief - $date

> Auto-generated by mino workplace brief

## 1. Major Financial Cases / Risk Events



## 2. Interest Rate Changes



## 3. Financial Innovation



## 4. New Policies / Regulations



---
*Generated at $(Get-Date -Format 'HH:mm') | mino.ps1*
"@
    if (-not $script:DryRun) {
        $template | Out-File -FilePath $outputFile -Encoding UTF8
        Write-Mino "Template saved: $outputFile" -Level SUCCESS
    }
}

